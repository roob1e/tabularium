package com.assxmblxr.backend.entity;

/**
 * Тип работы для оценки согласно ТЗ:
 * «ведение электронного журнала — выставление текущих и итоговых оценок, фиксация типа работы»
 */
public enum WorkType {
  CURRENT,          // Текущая оценка
  CONTROL,          // Контрольная работа
  INDEPENDENT,      // Самостоятельная работа
  TEST,             // Зачёт
  EXAM,             // Экзамен
  FINAL             // Итоговая оценка
}